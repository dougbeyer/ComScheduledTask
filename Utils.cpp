void Utils::log( const _TCHAR * pszFmt, ... )
  {
  if ( NULL == pszFmt )
    return;

  va_list args;
  va_start( args, pszFmt );

  _TCHAR buffer[ 256 ] = {};
  _vsntprintf_s( buffer, 256, _TRUNCATE, pszFmt, args );

  // Write error to stdout.
  COUT << buffer;

  // **TODO**: DSB, 08/11/2018 - Log error in debug log.

  va_end( args );
  }



